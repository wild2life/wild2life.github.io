/* ?? 运算符 */
const a = 0 ?? 'wildlife' // 0
const b = '' ?? 'wildlife' // ''
const c = null ?? 'wildlife' // 'wildlife'
const d = undefined ?? 'wildlife' // 'wildlife'

/* || 运算符 */
const A = 0 || 'wildlife' // 'wildlife'
const B = '' || 'wildlife' // 'wildlife'
const C = null || 'wildlife' // 'wildlife'
const D = undefined || 'wildlife' // 'wildlife'
